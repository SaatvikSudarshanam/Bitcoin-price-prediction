    def predict_next_price(self):
        print("ğŸ”® Predicting next price...")

        if not self.results:
            print("âŒ No trained models available")
            return None

        try:
            # Get best model
            best_model_name = min(self.results.keys(), key=lambda x: self.results[x]['RMSE'])
            best_model = self.models[best_model_name]

            # Get latest features
            latest_features = self.features_df[self.test_data['feature_cols']].iloc[-1:].values

            # Scale if needed
            if best_model_name == 'Linear Regression':
                latest_features = self.test_data['scaler'].transform(latest_features)

            # Predict
            prediction = float(best_model.predict(latest_features)[0])
            current_price = float(self.data['Close'].iloc[-1])
            change_pct = ((prediction - current_price) / current_price) * 100

            print(f"ğŸ¯ Best Model: {best_model_name}")
            print(f"ğŸ’° Current Price: ${current_price:.2f}")
            print(f"ğŸ“ˆ Predicted Next Price: ${prediction:.2f}")
            print(f"ğŸ“Š Expected Change: {change_pct:+.2f}%")

            return {
                'model': best_model_name,
                'current_price': current_price,
                'predicted_price': prediction,
                'change_percent': change_pct
            }
        except Exception as e:
            print(f"âŒ Error making prediction: {e}")
            return None

    def generate_trading_signal(self):
        print("ğŸ¯ Generating trading signals...")

        if self.features_df is None:
            print("âŒ No features available for signal generation")
            return ["HOLD"]

        try:
            latest_data = self.features_df.iloc[-1]
            signals = []

            # RSI signal
            if 'RSI' in latest_data and not pd.isna(latest_data['RSI']):
                rsi = latest_data['RSI']
                if rsi < 30:
                    signals.append("BUY (Oversold)")
                elif rsi > 70:
                    signals.append("SELL (Overbought)")

            # Moving average signal
            if 'Close_SMA_20_ratio' in latest_data and not pd.isna(latest_data['Close_SMA_20_ratio']):
                ma_ratio = latest_data['Close_SMA_20_ratio']
                if ma_ratio > 1.02:
                    signals.append("BUY (Above MA)")
                elif ma_ratio < 0.98:
                    signals.append("SELL (Below MA)")

            if not signals:
                signals.append("HOLD")

            print(f"ğŸ“Š Trading Signals: {', '.join(signals)}")
            return signals
        except Exception as e:
            print(f"âŒ Error generating signals: {e}")
            return ["HOLD"]

    def risk_analysis(self):
        print("âš ï¸ Performing risk analysis...")

        if self.data is None:
            print("âŒ No data available for risk analysis")
            return None

        try:
            returns = self.data['Close'].pct_change().dropna()

            # Value at Risk (VaR)
            var_95 = np.percentile(returns, 5) * 100
            var_99 = np.percentile(returns, 1) * 100

            # Volatility metrics
            current_vol = returns.std() * np.sqrt(252) * 100  # Annualized

            # Maximum drawdown
            cumulative_returns = (1 + returns).cumprod()
            peak = cumulative_returns.expanding().max()
            drawdown = (cumulative_returns - peak) / peak
            max_drawdown = drawdown.min() * 100

            risk_metrics = {
                'VaR_95': var_95,
                'VaR_99': var_99,
                'Annual_Volatility': current_vol,
                'Max_Drawdown': max_drawdown
            }

            print(f"ğŸ“Š 95% VaR (daily): {var_95:.2f}%")
            print(f"ğŸ“Š 99% VaR (daily): {var_99:.2f}%")
            print(f"ğŸ“Š Annual Volatility: {current_vol:.2f}%")
            print(f"ğŸ“Š Max Drawdown: {max_drawdown:.2f}%")

            return risk_metrics
        except Exception as e:
            print(f"âŒ Error in risk analysis: {e}")
            return None

